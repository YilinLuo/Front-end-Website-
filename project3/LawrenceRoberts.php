				<div class= "Background-Lawrence">
				<h2>Background</h2>
				<p>Lawrence Gilman Roberts (December 21, 1937 – December 26, 2018) was an American engineer who 
					received the Draper Prize in 2001 "for the development of the Internet", and the 
					Principe de Asturias Award in 2002.</p>
		    <p>As a program manager and office director at the Advanced Research Projects Agency, Roberts 
					and his team created the ARPANET using packet switchingtechniques invented by 
					British computer scientist Donald Davies and American Paul Baran underpinned by 
					the theoretical work of Leonard Kleinrock. The ARPANET, which was built by 
					the Massachusetts-based company Bolt Beranek and Newman (BBN), 
					was a predecessor to the modern Internet. He later served as 
					CEO of the commercial packet-switching network Telnet.</p>
				</div>

				</section>

<section class= "Early-Life-Edu-Lawrence">
<h2>Early life and Education</h2>
<p>Roberts, who was known as Larry, was born and raised in Westport, Connecticut. He was the 
	son of Elizabeth (Gilman) and Elliott John Roberts, both of whom had doctorates in chemistry. 
	During his youth, he built a Tesla coil, assembled a television, and designed a telephone network 
	built from transistors for his parents' Girl Scout camp.</p>
<p>Roberts attended the Massachusetts Institute of Technology (MIT), where he received his bachelor's degree (1959), 
	master's degree (1960), and Ph.D. (1963), all in electrical engineering.</p>
</section>

<section class= "career-Lawrence">
<h2>Career</h2>
<p>After receiving his PhD, Roberts continued to work at the MIT Lincoln Laboratory. 
	Having read the seminal 1961 paper of the "Intergalactic Computer Network" by J. C. R. Licklider, 
	Roberts developed a research interest in time-sharing using computer networks.</p>

		<p>In 1967, he was recruited by Robert Taylor in the ARPA Information Processing Techniques Office (IPTO)
			 to become the program manager for the ARPANET. Wesley A. Clark suggested the use of a dedicated computer, 
			 called the Interface Message Processor at each node of the network instead of centralized control. 
			 Shortly afterwards, at the 1967 ACM Symposium on Operating System Principles, Roberts met a member 
			 of Donald Davies's team (Roger Scantlebury) who presented their research on packet switching and 
			 suggested it for use in the ARPANET. Roberts applied Davies's concepts of packet switching for the 
			 ARPANET, and sought input from Paul Baran and Leonard Kleinrock. Subsequently, Roberts developed the 
			 plan for the ARPANET, the first wide area packet-switching network with distributed control across 
			 multiple computers, and managed its implementation. ARPA issued a request for quotation (RFQ) to build
			 the system, which was awarded to Bolt, Beranek and Newman (BBN). When Robert Taylor was sent to Vietnam 
			 in 1969 and then resigned, Roberts became director of the IPTO.</p>

		<p>In 1973, Roberts left ARPA to commercialize the nascent packet-switching technology in the form of Telenet, 
			the first packet switch utility company, and served as its CEO from 1973 to 1980. In 1983 he joined DHL 
			Corporation as President and CEO. He was CEO of NetExpress, an Asynchronous Transfer Mode (ATM) equipment 
			company, from 1983 to 1993. Roberts was president of ATM Systems from 1993 to 1998. He was chairman and 
			CTO of Caspian Networks, but left in early 2004; Caspian ceased operation in late 2006.</p>

		<p>As of 2011, Roberts was the founder and chairman of Anagran Inc. Anagran continues work in the same area 
			as Caspian: IP flow management with improved quality of service for the Internet.</p>

		<p>Since September 2012, he was CEO of Netmax in Redwood City, California.</p>

		<h2>Personal Life</h2>
		<p>Roberts married and divorced four times. At the time of his death, his partner was physician Tedde Rinker. 
			Roberts died at his California home from a heart attack on December 26, 2018.</p>
</section>


<section class = "ARPANET-Lawrence">
		<h2>ARPANET and the creation of the internet</h2>
		<!--section from http://articles.latimes.com/1999/oct/25/news/ss-26185-->
	
			<figure>
				<img src="images/robertskleinrock.jpg" alt="Game press for Assassin's Creed Odyssey.">
				<figcaption>fig. 2: Larry Roberts alongside his colleagues Leonard Kleinrock and Paul Baran. Baran created 
					packetswitching, and Roberts and Kleinrock implemented it in their work to create the precursor to the web, ARPANET.</figcaption>
			</figure>
	
			<!--Image found at https://photos.vanityfair.com/2015/01/30/54cbf95944a199085e88fc6c_image.jpg-->
	
			<p>The original vision for the Internet came from a behavioral scientist. In the early 1960s, J.C.R. Licklider
				 described how a "galactic network" of computers could not only spit out data but actually help people understand, 
				 analyze and solve problems. "He didn't know how to do it, but he thought it was important to connect all the 
				 machines together," said Larry Roberts, who succeeded Licklider as a director at the Defense Department's 
				 Advanced Research Projects Agency (ARPA).</p>
	
			<p>Inspired by Licklider, Roberts devised a plan in 1967 for building such a network. The Arpanet, as it was known,
				 would allow researchers to share their computers at various college campuses. A key part of the plan involved 
				 transferring computer data in small "packets" that could move more efficiently along the least-congested routes. 
				 The notion of sending data in packets came from Leonard Kleinrock.</p>
	
			<p>In 1969, Kleinrock, a UCLA professor, took possession of a refrigerator-sized machine made by Bolt Beranek & 
				Newman (BBN) called an Interface Message Processor. The job of the machine, known as an IMP, was to translate 
				data from various computers into a common network language. The IMP became the first outpost on the Arpanet.</p>
	
			<p>By 1972, many more computers had been added to the network and its proud parents were ready to show it off. 
				Bob Kahn, responsible for the system design at BBN in Cambridge, Mass., organized a large demonstration. 
				Sitting in Washington, D.C., Kleinrock's team logged onto a computer at BBN, shipped a program from Cambridge 
				to Los Angeles, ran the program on UCLA's computers and then had the results transmitted back to D.C. "[Computer 
				scientists] saw this happening for the first time, and there was a great deal of enthusiasm," Kleinrock said.</p>

</section>

