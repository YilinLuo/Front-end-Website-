<p>Bezos was born Jeffrey Preston Jorgensen on January 12, 1964, in Albuquerque, New Mexico, the son of Jacklyn Gise Jorgensen and Chicago, Illinois, native Ted Jorgensen. At the time of his birth, his mother was a 17-year-old high school student, and his father was a bike shop owner. After Jacklyn divorced Ted, she married Cuban immigrant Miguel "Mike" Bezos in April 1968. Shortly after the wedding, Mike adopted four-year-old Jorgensen, whose surname was then changed to Bezos. The family moved to Houston, Texas, where Mike worked as an engineer for Exxon after he received a degree from the University of New Mexico. Bezos attended River Oaks Elementary School in Houston from fourth to sixth grade. 
</p>

<p>Bezos is the maternal grandson of Lawrence Preston Gise, a regional director of the U.S. Atomic Energy Commission (AEC) in Albuquerque. Gise retired early to his family's ranch near Cotulla, Texas, where Bezos would spend many summers in his youth. Bezos would later purchase this ranch and grow it from 25,000 acres (10,117 ha) to 300,000 acres (121,406 ha). His maternal grandmother was Mattie Louise Gise (née Strait), through whom he is a cousin of country singer George Strait. Bezos often displayed scientific interests and technological proficiency; he once rigged an electric alarm to keep his younger siblings out of his room. 
</p>

<p>The family moved to Miami, Florida, where Bezos attended Miami Palmetto High School. While Bezos was in high school, he worked at McDonald's as a short-order line cook during the breakfast shift. He attended the Student Science Training Program at the University of Florida. He was high school valedictorian, a National Merit Scholar, and a Silver Knight Award winner in 1982. In 1986, he graduated from Princeton University with a 4.2 grade point average and Bachelor of Science degrees in electrical engineering and computer science and was a member of Phi Beta Kappa. While at Princeton, he was also elected to Tau Beta Pi and was the president of the Princeton chapter of the Students for the Exploration and Development of Space. 
</p>
	</section>

<section class= "Start-Amazon-Bezos">
		<h2>Start of Jeff's Career and Amazon</h2>
	
		<figure>
		
		<img src="images/amazon.png" alt="Amazon">
		
		<figcaption>Jeff's company, Amazon.</figcaption>
		
		</figure>
		
			<p>After Bezos graduated from Princeton in 1986, he was offered jobs at Intel, Bell Labs, and Andersen Consulting, among others. He first worked at Fitel, a fintechtelecommunications start-up, where he was tasked with building a network for international trade. Bezos was promoted to head of development and director of customer service thereafter. He transitioned into the banking industry when he became a product manager at Bankers Trust; he worked there from 1988 to 1990. He then joined D. E. Shaw & Co, a newly founded hedge fund, in 1990 and worked there until 1994. Bezos became D. E. Shaw's fourth senior vice-president at the age of 30.
			</p>
		
			<p>In late 1993, Bezos decided to start an online bookstore. He left his job at D. E. Shaw and founded Amazon in his garage on July 5, 1994, after writing its business plan on a trip from New York to Seattle. Bezos named his new company Amazonafter the Amazon River in South America, in part because the name begins with the letter A, which is at the beginning of the alphabet. He accepted an estimated $300,000 from his parents and invested in Amazon. He warned many early investors that there was a 70% chance that Amazon would fail or go bankrupt. Although Amazon was originally an online bookstore, Bezos had always planned to expand to other products. Three years after Bezos founded Amazon, he took it public with an initial public offering (IPO). In response to critical reports from Fortune and Barron's, Bezos maintained that the growth of the internet would overtake competition from larger book retailers such as Borders and Barnes & Noble. 
			</p>
		
			<p>In 1998, Bezos diversified into the online sale of music and video; by the end of the year, he had also expanded the company's products to include a variety of consumer goods. Bezos used the $54 million raised during the company's 1997 equity offering to finance aggressive acquisition of smaller competitors. In 2002, Bezos led Amazon to launch Amazon Web Services, which compiled data from weather channels and website traffic. In late 2002, rapid spending from Amazon caused it financial distress when revenues stagnated. In 2000, Bezos borrowed $2 billion from banks, as its cash balances dipped to only $350 million. After the company nearly went bankrupt, he closed distribution centers and laid off 14% of the Amazon workforce. In 2003, Amazon rebounded from financial instability and turned a profit of $400 million. In November 2007, Bezos launched the Amazon Kindle. According to a 2008 Time profile, Bezos wished to create the same flow state found in video game simulations in books; he wished readers would fully engage with books. In 2013, Bezos secured a $600 million contract with the Central Intelligence Agency (CIA) on behalf of Amazon Web Services. In October that year, Amazon was recognized as the largest online shopping retailer in the world. Amazon also became a platform for sellers to be able to make sales directly through amazon.
			</p>
</section>


<section class = "Wealth-Bezos">
		<h2>Wealth</h2>
	
		<p>Bezos first became a millionaire in 1997 after raising $54 million through Amazon's initial public offering(IPO). He was first included on Forbes The World's Billionaires list in 1999 with a registered net worth of $10.1 billion. His net worth decreased to $6.1 billion a year later, a 40.5% drop. His wealth plummeted even more the following year, dropping 66.6% to $2.0 billion. He lost $500 million the following year, which brought his net worth down to $1.5 billion. The following year, his net worth increased by 104% to $2.5 billion. From 2005 to 2007, he quadrupled his net worth to $8.7 billion. After the financial crisis and succeeding economic recession, his net worth would erode to $6.8 billion—a 17.7% drop. His wealth rose by 85.2% in 2010, leaving him with $12.6 billion. This percentage increase ascended him to the 43rd spot on the ranking from 68th.
		</p>
	
		<p>After a rumor broke out that Amazon was developing a smartphone, Bezos's net worth rose to $30.5 billion in 2014. A year later, Bezos entered the top ten when he increased his net worth to a total of $50.3 billion. Bezos rose to be the 5th richest person in the world hours before market close; he gained $7 billion in one hour. By the time the Forbes list was calculated in March 2016, his net worth was registered at $45.2 billion. However, just months later in October 2016, his wealth increased by $16.2 billion to $66.5 billion unofficially ranking him the third richest person in the world behind Warren Buffett. After sporadic jumps in Amazon's share price, in July 2017 he briefly unseated Microsoft cofounder Bill Gates as the wealthiest person in the world.
		</p>
	
		<p>Bezos would continue to sporadically surpass Gates throughout the month of October 2017 after Amazon's share price fluctuated. His net worth surpassed $100 billion for the first time on November 24, 2017, after Amazon's share price increased by more than 2.5%. When the 2017 list was issued, Bezos's net worth was registered at $72.8 billion, adding $27.6 billion from the previous year. Bezos was officially ranked as the third wealthiest person in the world up from the 5th spot in 2016. His wealth's rapid growth from 2016 to 2017 sparked a variety of assessments about how much money Bezos earned on a controlled, reduced time scale. On October 10, 2017, he made an estimated $6.24 billion in 5 minutes, slightly less than the annual Gross Domestic Product of Kyrgyzstan. 
		</p>
	
		<p>On March 6, 2018, Bezos was officially designated the wealthiest person in the world with a registered net worth of $112 billion. He unseated Bill Gates ($90 billion) who was $6 billion ahead of Warren Buffett ($84 billion), ranked third. He is considered the first registered centi-billionaire (not adjusted for inflation). 
		</p>
	
		<p>His wealth, in 2017–18 terms, equaled that of 2.7 million Americans. Bezos's net worth increased by $33.6 billion from January 2017 to January 2018. This increase outstripped the economic development (in GDP terms) of more than 96 countries around the world. During March 9, Bezos earned $230,000 every 60 seconds. The Motley Fool estimated that if Bezos had not sold any of his shares from its original public offering in 1997, his net worth would sit at $181 billion in 2018. According to Quartz, his net worth of $150 billion in July 2018 was enough to purchase the entire stock markets of Nigeria, Hungary, Egypt, Luxembourg, and Iran. Following the report by Quartz, Amazon workers in Poland, Germany, and Spain participated in demonstrations and labor strikes to draw awareness to his growing wealth and the lack of compensation, labor rights, and satisfactory working conditions of select Amazon workers. On July 17, 2018 he was designated the "wealthiest person in modern history "by the Bloomberg Billionaires Index, Fortune, MarketWatch, The Wall Street Journal, and Forbes. 
		</p>
</section>

<section class= "Recognition-Bezos">
		<h2>Recognition</h2>

		<p>In 1999, Bezos received his first major award when Time named him Person of the Year. In 2008, he was selected by U.S. News & World Report as one of America's best leaders. Bezos was awarded an honorary doctorate in science and technology from Carnegie Mellon University in 2008. In 2011, The Economist gave Bezos and Gregg Zehr an Innovation Award for the Amazon Kindle. In 2012, Bezos was named Businessperson of the Year by Fortune. He is also a member of the Bilderberg Group and attended the 2011 Bilderberg conference in St. Moritz, Switzerland, and the 2013 conference in Watford, Hertfordshire, England. He was a member of the Executive Committee of The Business Council for 2011 and 2012. In 2014, he was ranked the best-performing CEO in the world by Harvard Business Review. 
		</p>
	
		<p>He has also figured in Fortune's list of 50 great leaders of the world for three straight years, topping the list in 2015. In September 2016, Bezos received a $250,000 prize for winning the Heinlein Prize for Advances in Space Commercialization, which he donated to the Students for the Exploration and Development of Space. In February 2018, Bezos was elected to the National Academy of Engineering for "leadership and innovation in space exploration, autonomous systems, and building a commercial pathway for human space flight". In March 2018, at the Explorers Club annual dinner, he was awarded the Buzz Aldrin Space Exploration Award in recognition of his work with Blue Origin. He received Germany's 2018 Axel Springer Award for Business Innovation and Social Responsibility. Time magazine named him one of the 100 most influential people in the world in their 2018 listing. 
		</p>

</section>

