<section class= "ACE">
<h2>ACE- Automatic Computing Engine</h2>
<p>
	Between 1945 and 1947, Turing lived in Hampton, London, while he worked on the design of the ACE 
	(Automatic Computing Engine) at the National Physical Laboratory (NPL). He presented a paper on 
	19 February 1946, which was the first detailed design of a stored-program computer. Von Neumann's 
	incomplete First Draft of a Report on the EDVAC had predated Turing's paper, but it was much less detailed 
	and, according to John R. Womersley, Superintendent of the NPL Mathematics Division, it "contains a number
	of ideas which are Dr. Turing's own". Although ACE was a feasible design, the secrecy surrounding the 
	wartime work at Bletchley Park led to delays in starting the project and he became disillusioned. 
	In late 1947 he returned to Cambridge for a sabbatical year during which he produced a seminal work on 
	Intelligent Machinery that was not published in his lifetime. While he was at Cambridge, the Pilot ACE
	was being built in his absence. It executed its first program on 10 May 1950, and a number of later 
	computers around the world owe much to it, including the English Electric DEUCE and the American Bendix G-15. 
	The full version of Turing's ACE was not built until after his death.
</p>

<p>
	According to the memoirs of the German computer pioneer Heinz Billing from the Max Planck Institute for Physics, 
	published by Genscher, Düsseldorf, there was a meeting between Turing and Konrad Zuse. It took place in Göttingen 
	in 1947. The interrogation had the form of a colloquium. Participants were Womersley, Turing, Porter from England 
	and a few German researchers like Zuse, Walther, and Billing (for more details see Herbert Bruderer, Konrad Zuse 
	und die Schweiz).
</p>

<p>
	Turing was appointed Reader in the Mathematics Department at the Victoria University of Manchester in 1948 and in 
	1949, became Deputy Director of the Computing Machine Laboratory there, working on software for one of the earliest 
	stored-program computers—the Manchester Mark 1. During this time he continued to do more abstract work in mathematics, 
	and in "Computing Machinery and Intelligence" (Mind, October 1950), Turing addressed the problem of artificial intelligence, 
	and proposed an experiment that became known as the Turing test, an attempt to define a standard for a machine to be 
	called "intelligent". The idea was that a computer could be said to "think" if a human interrogator could not tell it 
	apart, through conversation, from a human being. In the paper, Turing suggested that rather than building a program to simulate 
	the adult mind, it would be better rather to produce a simpler one to simulate a child's mind and then to subject it to a 
	course of education. A reversed form of the Turing test is widely used on the Internet; the CAPTCHA test is intended to 
	determine whether the user is a human or a computer.
</p>

<p>
	In 1948 Turing, working with his former undergraduate colleague, D.G. Champernowne, began writing a chess program for a 
	computer that did not yet exist. By 1950, the program was completed and dubbed the Turbochamp. In 1952, he tried to implement 
	it on a Ferranti Mark 1, but lacking enough power, the computer was unable to execute the program. Instead, Turing "ran" the 
	program by flipping through the pages of the algorithm and carrying out its instructions on a chessboard, taking about half 
	an hour per move. The game was recorded. According to Garry Kasparov, Turing's program "played a recognizable game of chess." 
	The program lost to Turing's colleague Alick Glennie, although it is said that it won a game against Champernowne's wife, Isabel.
</p>

<p>
	His Turing test was a significant, characteristically provocative, and lasting contribution to the debate regarding artificial 
	intelligence, which continues after more than half a century. He also invented the LU decomposition method in 1948, used today 
	for solving matrix equations.
</p>
</section>


<section class = "prosecution">
<h2>Persecution on Sexuality</h2>
<p>
	Turing was prosecuted in 1952 for homosexual acts, when by the Labouchere Amendment, "gross indecency" was a criminal offence 
	in the UK. He accepted chemical castration treatment, with DES, as an alternative to prison. Turing died in 1954, 16 days before 
	his 42nd birthday, from cyanide poisoning. An inquest determined his death as suicide, but it has been noted that the known evidence 
	is also consistent with accidental poisoning.
</p>

<p>
	In 2009, following an Internet campaign, British Prime Minister Gordon Brown made an official public apology on behalf of the British 
	government for "the appalling way he was treated". Queen Elizabeth II granted him a posthumous pardon in 2013. The Alan Turing law 
	is now an informal term for a 2017 law in the United Kingdom that retroactively pardoned men cautioned or convicted under historical 
	legislation that outlawed homosexual acts.
</p>
</section>

