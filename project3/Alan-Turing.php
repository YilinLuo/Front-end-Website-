<div class= "text-turing-machine">
<h2>Contribution to WWII</h2>
<p>
	During the Second World War, Turing worked for the Government Code and Cypher School 
	(GC&CS) at Bletchley Park, Britain's codebreaking centre that produced Ultra intelligence. 
	For a time he led Hut 8, the section that was responsible for German naval cryptanalysis. 
	Here he devised a number of techniques for speeding the breaking of German ciphers, 
	including improvements to the pre-war Polish bombe method, an electromechanical machine 
	that could find settings for the Enigma machine.
</p>

<p>
	Turing played a pivotal role in cracking intercepted coded messages that enabled the 
	Allies to defeat the Nazis in many crucial engagements, including the Battle of the Atlantic, 
	and in so doing helped win the war. Counterfactual history is difficult with respect to the effect 
	Ultra intelligence had on the length of the war, but at the upper end it has been estimated 
	that this work shortened the war in Europe by more than two years and saved over 14 million lives.
</p>

<h2>Post WWII- Further Progress on Computer Prototypes</h2>

<p>
	After the war, Turing worked at the National Physical Laboratory, where he designed the ACE, 
	among the first designs for a stored-program computer. In 1948 Turing joined Max Newman's 
	Computing Machine Laboratory at the Victoria University of Manchester, where he helped develop 
	the Manchester computers and became interested in mathematical biology. He wrote a paper on the 
	chemical basis of morphogenesis[2] and predicted oscillating chemical reactions such as the 
	Belousov–Zhabotinsky reaction, first observed in the 1960s.
</p>
